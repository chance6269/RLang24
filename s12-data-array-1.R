# Array: 다차원 배열
# 행, 열, 면
# 생성함수 : array()

c12 <- c(1:12)
adm <- c(3,2,2)
ax <- array(c12, adm)
ax

# , , 1 (1면)
# 
#       [,1] [,2]
# [1,]    1    4
# [2,]    2    5
# [3,]    3    6
# 
# , , 2 (2면)
# 
#       [,1] [,2]
# [1,]    7   10
# [2,]    8   11
# [3,]    9   12

mode(ax)
class(ax)

# 면 참조
ax[,,1]
ax[,,2]

# 열단위
ax[,1,1] # 1열 1면: 1 2 3
ax[,2,1] # 2열 1면: 4 5 6
ax[,1,2] # 1열 2면: 7 8 9
ax[,2,2] # 2열 2면: 10 11 12

# 행단위
ax[1,,1] # 1행 1면: 1 4
ax[2,,1] # 2행 1면: 2 5
ax[3,,1] # 3행 1면: 3 6
ax[1,,2] # 1행 2면: 7 10
ax[2,,2] # 2행 2면: 8 11
ax[3,,2] # 3행 2면: 9 12

# 범위 지정
ax[1,,1:2] # 1행, 모든열, 1~2면
#       [,1] [,2]
# [1,]    1    7
# [2,]    4   10